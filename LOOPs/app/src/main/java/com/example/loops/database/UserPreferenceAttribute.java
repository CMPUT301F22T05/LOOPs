package com.example.loops.database;

/**
 * Attributes of user preferences. They can be manipulated by the user
 * IngredientCategory - the categories of the ingredients
 * RecipeCategory - the categories of the recipes
 * StorageLocation - location of the ingredients to store
 */
public enum UserPreferenceAttribute {
    IngredientCategory,
    RecipeCategory,
    StorageLocation,
}
